static inline int32_t alu_mul_asm(int32_t a, int32_t b)
{
    int32_t r;

    __asm__ volatile (
        "mul %0, %1, %2"
        : "=r"(r)
        : "r"(a), "r"(b)
    );

    return r;
}

extern "C" void run()
{
    uint32_t error_count = 0u;

    constexpr uint32_t N =
        static_cast<uint32_t>(
            sizeof(test_vals) / sizeof(test_vals[0])
        );

    for (uint32_t i = 0u; i < N; ++i) {
        for (uint32_t j = 0u; j < N; ++j) {
            const int32_t a = test_vals[i];
            const int32_t b = test_vals[j];

            const uint32_t actual =
                static_cast<uint32_t>(alu_mul_asm(a, b));

            const uint32_t expected =
                expected_table[i][j];

            PIO32 = actual;

            if (actual != expected) {
                PIO32 = ERROR_MARKER;
                PIO32 = i;
                PIO32 = j;
                PIO32 = static_cast<uint32_t>(a);
                PIO32 = static_cast<uint32_t>(b);
                PIO32 = actual;
                PIO32 = expected;

                ++error_count;
            }
        }
    }

    result = error_count;

    PIO32 = (error_count == 0u)
        ? TEST_PASS_CODE
        : TEST_FAIL_CODE;

    PIO32 = TEST_END_CODE;
    PIO32 = result;

    while (1) {
    }
}

