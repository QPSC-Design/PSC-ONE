#include <cstdint>

/* ---------- アサーション用PIO出力 ---------- */
#define PIO32 (*reinterpret_cast<volatile uint32_t*>(0x10001000u))
static constexpr uint32_t TEST_END_CODE = 0xEE01;

/* ---------- 宣言：extern（初期化しない） ---------- */
extern "C" volatile uint32_t result;

/* ============================================================
 * run() —— BootROM から直接呼ばれる唯一のエントリ
 * ============================================================ */
extern "C" void run() {
    uint32_t r = 0;

    // ------------------------------------------------------------
    // テスト内容：
    //   PC=0 から実行されることを想定して
    //   JAL によって +8 へジャンプし、
    //   そこに置いたリテラルを読み取る
    // ------------------------------------------------------------

    asm volatile(
        // 0x0000: jal x0, +8
        ".word 0x0080006f\n"   // JAL x0, 8   → PC = 0x0008 へジャンプ

        // 0x0004: addi x0,x0,0 (NOP) — ここは飛び越える
        ".word 0x00000013\n"

        // 0x0008: li %0, 0x12345678
        "li %0, 0x12345678\n"

        : "=r"(r)
    );

    // 結果を格納
    result = r;

    // 結果通知
    PIO32 = TEST_END_CODE;
    PIO32 = result;

    // 無限ループ
    while (1) { }
}

/* ---------- 実体定義 ---------- */
extern "C" {
    volatile uint32_t result = 0;
}
