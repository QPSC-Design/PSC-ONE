/*============================================================
    MUL
============================================================*/

int32_t fft_mul(int32_t a, int32_t b)
{
    int32_t r;

    __asm__ volatile (
        "mul %0,%1,%2"
        : "=r"(r)
        : "r"(a),"r"(b)
    );

    return r;
}

/*============================================================
    Complex Add
============================================================*/

fft_complex_t fft_add(
    fft_complex_t a,
    fft_complex_t b)
{
    fft_complex_t c;

    c.re = a.re + b.re;
    c.im = a.im + b.im;

    return c;
}

/*============================================================
    Complex Sub
============================================================*/

fft_complex_t fft_sub(
    fft_complex_t a,
    fft_complex_t b)
{
    fft_complex_t c;

    c.re = a.re - b.re;
    c.im = a.im - b.im;

    return c;
}

/*============================================================
    Complex Multiply (Q15)
============================================================*/

fft_complex_t fft_mul_q15(
    fft_complex_t a,
    fft_complex_t b)
{
    fft_complex_t c;

    int32_t ac = fft_mul(a.re,b.re);
    int32_t bd = fft_mul(a.im,b.im);
    int32_t ad = fft_mul(a.re,b.im);
    int32_t bc = fft_mul(a.im,b.re);

    //c.re = (int16_t)((ac-bd)>>15);
    //c.im = (int16_t)((ad+bc)>>15);
    c.re = (ac - bd) >> 15;
    c.im = (ad + bc) >> 15;

    return c;
}

/*============================================================
    Bit Reverse
============================================================*/

static void fft_bit_reverse(
    fft_complex_t *x,
    int n)
{
    int i,j,k;

    j=0;

    for(i=1;i<n;i++)
    {
        k=n>>1;

        while(j>=k)
        {
            j-=k;
            k>>=1;
        }

        j+=k;

        if(i<j)
        {
            fft_complex_t t=x[i];
            x[i]=x[j];
            x[j]=t;
        }
    }
}

/*============================================================
    FFT
============================================================*/

int fft_q15(
    fft_complex_t *x,
    int n)
{
    fft_bit_reverse(x, n);

    for (int len = 2; len <= n; len <<= 1)
    {
        int half = len >> 1;

        /* Twiddle step */
        int step = FFT_MAX_SIZE / len;

        for (int i = 0; i < n; i += len)
        {
            int k = 0;

            for (int j = 0; j < half; j++)
            {
                fft_complex_t u;
                fft_complex_t v;
                fft_complex_t t;

                u = x[i + j];

                v = x[i + j + half];

                /* Twiddle */
                t = fft_mul_q15(
                        v,
                        fft_twiddle[k]);

                x[i + j] =
                    fft_add(u, t);

                x[i + j + half] =
                    fft_sub(u, t);

                k += step;
            }
        }
    }

    return 0;
}